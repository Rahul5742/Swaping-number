public class Swaping_number_using_3rd_variable {
public static void main(String[] args) {

    System.out.println("Swaping number using 3rd variable :");

    int a=10,b=20,sum;

    //Swaping logic : using 3rd variable
    sum=a;
    a=b;
    b=sum;

    System.out.println("a :"+a);
    System.out.println("b :"+b);


}

}
